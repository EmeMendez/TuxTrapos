    public void CargarDetalle(String nro){


        String [] Titulo = {"Codigo","Descripcion","Cantidad","Precio", "Valor total"};
        tabla=new DefaultTableModel(null,Titulo);
        Object [] fila= new Object[5];
        DetVentaC miC = new DetVentaC();
        ArrayList<DetVenta>Registro= miC.MuestaDetalle(nro);
        try {
            for (int i =0; i < Registro.size(); i++ )
            {
                fila[0]=Registro.get(i).getMiarticulo().getArtID();
                fila[1]=Registro.get(i).getMiarticulo().getArtNombre();
                fila[2]=Registro.get(i).getDetVcantidad();
                fila[3]=Registro.get(i).getDetVprecio();
                fila[4]=Registro.get(i).getDetVtotal();
                
                tabla.addRow(fila);
            }
            t.setModel(tabla);
        } catch (Exception ex) {
            Logger.getLogger(ArticuloV.class.getName()).log(Level.SEVERE, null, ex);
        }
    }    

