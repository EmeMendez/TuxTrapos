        public void listarven(){
            VentaC miControl=new VentaC();
          
            if( miControl.siExisteVenta(txtVnro.getText())){
            JOptionPane.showMessageDialog(null, "Venta existente");
            
            Venta miven= miControl.recupera(txtVnro.getText());
            this.CargarDetalle(txtVnro.getText());
            txtVnro.setText(miven.getTranId());
            txtVcomision.setText(miven.getVenComision());
            txtVtotal.setText(miven.getTranValor());
            txtVrutCliente.setText(miven.getMiCliente().getPerID());
            txtVnombreCliente.setText(miven.getMiCliente().getPerNombre());
            txtVrutEmpleado.setText(miven.getMiEmpleado().getPerID());
            txtVnombreEmpleado.setText(miven.getMiEmpleado().getPerNombre());
            txtVrutProv.setText(miven.getMiProveedor().getPerID());
            txtVProNombre.setText(miven.getMiProveedor().getPerNombre());
            txtMedioPago.setText(miven.getTranMedioPago());
            
            
       }
       else{
            JOptionPane.showMessageDialog(null, "Venta no existe");
            limpiarCampos();
       }
    }
     
