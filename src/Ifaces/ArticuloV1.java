    private void AnadirArtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AnadirArtActionPerformed
                   
    try {
     
         DefaultTableModel tabladet = (DefaultTableModel) CompraV1.tbl.getModel();
         String[]  dato=new String[7];
         
         int  fila = tblArt2.getSelectedRow();
       
         if(fila==-1)
         {
             JOptionPane.showMessageDialog(null, "No  ha seleccionado ningun registro");
         }
         
         else
          {
          String artId=tblArt2.getValueAt(fila, 0).toString();
          String artNombre=tblArt2.getValueAt(fila, 1).toString();
          String artProveedor=tblArt2.getValueAt(fila, 5).toString();
          String artTipo=tblArt2.getValueAt(fila, 4).toString();
          String artPrecio=tblArt2.getValueAt(fila, 2).toString();
          String CantIngresada=JOptionPane.showInputDialog("ingrese cantidad");
          int total=Integer.parseInt(CantIngresada)*Integer.parseInt(artPrecio);
          String CantExistente=tblArt2.getValueAt(fila, 3).toString();
          
          if(Integer.parseInt(CantIngresada)<=Integer.parseInt(CantExistente) && Integer.parseInt(CantIngresada)!=0 && Integer.parseInt(CantIngresada)>0 )
          {
            dato[0]=artId;
            dato[1]=artNombre;
            dato[2]=artProveedor;
            dato[3]=artTipo;
            dato[4]=CantIngresada;
            dato[5]=artPrecio;
            dato[6]=Integer.toString(total);

          
            tabladet.addRow(dato);
        
            VentaV1.tb.setModel(tabladet);
          }
          else {
              JOptionPane.showMessageDialog(null, "ingrese una cantidad válida o revise el stock");
          }
        }
         
    
    }
    catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null,"Ingrese una cantidad válida");
            }
                                              

    }//GEN-LAST:event_AnadirArtActionPerformed

