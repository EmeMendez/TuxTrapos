    public void cargarArticulos(String busqueda){
        String [] Titulo = {"Codigo","Descripcion","Precio","Stock","Tipo","Proveedor"};
        mitabla=new DefaultTableModel(null,Titulo);
        tblArt2.setModel(mitabla);
        try{
        ArticuloC artC = new ArticuloC();
        ArrayList<Articulo> Lista=artC.ArticulosPorProveedor(busqueda,CompraV1.txtProID.getText());
        Object []fila= new Object[6];
        
        for(int i =0; i < Lista.size(); i++){
                fila[0]=Lista.get(i).getArtID();
                fila[1]=Lista.get(i).getArtNombre();
                fila[2]=Lista.get(i).getArtPrecio();
                fila[3]=Lista.get(i).getArtStock();
                fila[4]=Lista.get(i).getMiCategoria().getCat_nombre();
                fila[5]=Lista.get(i).getMiProveedor().getPerNombre();
                       
                mitabla.addRow(fila);          
            }
        }catch(Exception e){    
            tblArt2.setModel(mitabla);

        }
    }
    
    
    
    
    
    
    
    
    

