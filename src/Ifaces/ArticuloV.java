        public void CargarProductos(){

        String [] Titulo = {"Codigo","Descripcion","Precio","Stock"};
        tabla=new DefaultTableModel(null,Titulo);
        try {
        ArticuloC artC = new ArticuloC();
        ArrayList<Articulo> Lista=artC.MuestraArticulos(txtArt.getText());
        Object []fila= new Object[4];
        
        for(int i =0; i < Lista.size(); i++){
                fila[0]=Lista.get(i).getArtID();
                fila[1]=Lista.get(i).getArtNombre();
                fila[2]=Lista.get(i).getArtPrecio();
                fila[3]=Lista.get(i).getArtStock();
                       
                tabla.addRow(fila);
        }
       
           tblArt.setModel(tabla);
        }
        catch(Exception e ){
         JOptionPane.showMessageDialog(null, "no se mostrar" + e );
        }

    }    
    
 

