    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
           
    try {
     
         DefaultTableModel tabladet = (DefaultTableModel) VentaV1.tb.getModel();
         String[]  miMatriz=new String[5];
         
         int  fila = tblArt.getSelectedRow();
       
         if(fila==-1)
         {
             JOptionPane.showMessageDialog(null, "No  ha seleccionado ningun registro");
         }
         
         else
          {
          String artId=tblArt.getValueAt(fila, 0).toString();
          String artNombre=tblArt.getValueAt(fila, 1).toString();
          String artPrecio=tblArt.getValueAt(fila, 2).toString();
          String CantExistente=tblArt.getValueAt(fila, 3).toString();
        try{
          String CantIngresada=JOptionPane.showInputDialog("ingrese cantidad");
          int total=Integer.parseInt(CantIngresada)*Integer.parseInt(artPrecio);
          
          if(Integer.parseInt(CantIngresada)<=Integer.parseInt(CantExistente) && Integer.parseInt(CantIngresada)!=0 && Integer.parseInt(CantIngresada)>0 )
          {
            miMatriz[0]=artId;
            miMatriz[1]=artNombre;
            miMatriz[2]=artPrecio;
            miMatriz[3]=CantIngresada;
            miMatriz[4]=Integer.toString(total);


          
            tabladet.addRow(miMatriz);
        
            VentaV1.tb.setModel(tabladet);
          }
          else {
              JOptionPane.showMessageDialog(null, "ingrese una cantidad válida o revise el stock");
          }
          }catch(NumberFormatException e){
              JOptionPane.showMessageDialog(null,"Ingrese cantidad válida");
              
          }
        }
         
    
    }
    catch (Exception e) {
        JOptionPane.showMessageDialog(null,"no se puede añadir el producto" + e);
            }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

